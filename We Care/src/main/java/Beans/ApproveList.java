package Beans;

public class ApproveList {

	String requestNo,description,sentByOrg,raiseBy,status;

	public String getRequestNo() {
		return requestNo;
	}

	public String getDescription() {
		return description;
	}

	public String getSentByOrg() {
		return sentByOrg;
	}

	public String getRaiseBy() {
		return raiseBy;
	}

	public String getStatus() {
		return status;
	}

	public void setRequestNo(String requestNo) {
		this.requestNo = requestNo;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public void setSentByOrg(String sentByOrg) {
		this.sentByOrg = sentByOrg;
	}

	public void setRaiseBy(String raiseBy) {
		this.raiseBy = raiseBy;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
